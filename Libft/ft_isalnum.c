#include "libft.h"

int isalnum(int c)
{
  // if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c >= 97 && c <= 122))
  if (ft_isalpha(c) || ft_isdigit(c))
    return (1);
  return (0);
}
